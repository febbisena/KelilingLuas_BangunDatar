print("======= Keliling dan Luas Persegi Panjang =======")
p = int(input("\nInputkan panjang persegi panjang = "))
l = int(input("Inputkan lebar persegi panjang = "))
keliling = 2 * (p + l)
luas = p * l
print("\nKeliling persegi panjang adalah ", keliling)
print("Luas persegi panjang adalah ", luas)